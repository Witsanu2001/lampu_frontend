// src/app/routes.tsx
import { Routes, Route, Navigate } from "react-router-dom";
import Home from "../modules/home/Home";
import Login from "../modules/login/Login";
import Order from "../modules/order/Order";
import Payment from "../modules/order/Payment";

interface AppRoutesProps {
  user: any;
  setUser: (user: any) => void;
}

export default function AppRoutes({ user, setUser }: AppRoutesProps) {
  return (
    <Routes>
      {/* ?? ??????? (?????????????????????) */}
      <Route 
        path="/" 
        element={user ? <Home /> : <Navigate to="/login" replace />} 
      />
      
      {/* ?? ??????????? (??????????????????????????????) */}
      <Route 
        path="/login" 
        element={!user ? <Login setUser={setUser} /> : <Navigate to="/" replace />} 
      />
      
      {/* ?? ??????????? (?????????????????????) */}
      <Route 
        path="/orders" 
        element={user ? <Order /> : <Navigate to="/login" replace />} 
      />

      {/* ?? ???????????? (?????????????????????) */}
      <Route 
        path="/payment" 
        element={user ? <Payment /> : <Navigate to="/login" replace />} 
      />
    </Routes>
  );
}
